// 1. Create a variable using let and print its value.
let name = "Ajeet";
console.log(name); // Ajeet

// 2. Declare a constant PI = 3.14 and print it.
const PI = 3.14;
console.log(PI); // 3.14

// 3. Declare a variable, reassign it, and print both values.
let a = 10;
a = 5;
console.log(a); // 5

// 4. Print the result of typeof null.
console.log(typeof null); // object

// 5. Store a number as a string and print its type.
let num = 123;
let mystring = num.toString();
console.log(mystring); // "123"

// 6. Create a boolean variable and print its type.
let boolean1 = true;
console.log(typeof boolean1); // boolean

// 7. Create a string, number, and boolean and print them together.
let str = "Ajeet";
let num1 = 5;
let bool = true;
console.log(str, num1, bool); // Ajeet 5 true

// 8. Declare a variable without assigning a value and print its type.
let c;
console.log(c); // undefined

// 9. Assign undefined to a variable and print its type.
c = undefined;
console.log(c); // undefined

// 10. Create an array using const, modify its elements, and print it.
const arr = ["Ajeet", "Singh", "Kuntal"];
arr[0] = "Ak";
arr[1] = "Jaat";
arr[2] = "Shab";
arr.push(5);
console.log(arr); //  ['AK', 'Jaat', 'Shab', 5]

